package br.com.projeto.beans;

public class NivelUsuario {
	private int codigo;
	private String nome;
	
	public NivelUsuario() {
		super();
	}

	public NivelUsuario(int codigo, String nome) {
		super();
		this.codigo = codigo;
		this.nome = nome;
	}
	
	public NivelUsuario(String nome) {
		super();
		this.nome = nome;
	}

	public String getTudo() {
		return codigo+"\n"+nome;
	}
	
	public void setTudo(int codigo, String nome, String descricao) {
		this.codigo = codigo;
		this.nome = nome;
	}
	
	public int getCodigo() {
		return codigo;
	}
	
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
}
